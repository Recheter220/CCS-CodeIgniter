CREATE DATABASE VAGA;

USE VAGA;

CREATE TABLE CV(
	ID_CV SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT,
	NOME VARCHAR(50) NOT NULL,
	SEXO CHAR(1),
	DATA_NASC DATE,
	EMAIL VARCHAR(50),
	CEP VARCHAR(8),
	SENHA VARCHAR(30),
	FOTO VARCHAR(100),
	PRIMARY KEY (ID_CV)
);

CREATE TABLE CIDADE (
	ID_CIDADE INT UNSIGNED NOT NULL AUTO_INCREMENT,
	DESCRICAO VARCHAR(50) NOT NULL,
	PRIMARY KEY (ID_CIDADE)
);

CREATE TABLE CARGO (
	ID_CARGO INT UNSIGNED NOT NULL AUTO_INCREMENT,
	DESCRICAO VARCHAR(30) NOT NULL,
	PRIMARY KEY (ID_CARGO)
);

CREATE TABLE AREA (
	ID_AREA INT UNSIGNED NOT NULL AUTO_INCREMENT,
	DESCRICAO VARCHAR(30) NOT NULL,
	PRIMARY KEY (ID_AREA)
);

CREATE TABLE VAGAS (
	ID SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT,
	DESCRICAO VARCHAR(50) NOT NULL,
	DEFICIENTE CHAR(1),
	RESUMO VARCHAR(200),
	EMPRESA VARCHAR(30),
	DATA_INSERCAO DATE NOT NULL,
	SALARIO VARCHAR(15) NOT NULL,
	CIDADE_ID INT UNSIGNED,
	CARGO_ID INT UNSIGNED,
	AREA_ID INT UNSIGNED,
	PRIMARY KEY (ID),
	FOREIGN KEY (CIDADE_ID) REFERENCES CIDADE(ID_CIDADE),
	FOREIGN KEY (AREA_ID) REFERENCES AREA(ID_AREA),
	FOREIGN KEY (CARGO_ID) REFERENCES CARGO(ID_CARGO)
);

CREATE TABLE USUARIO (
	ID SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT,
	USUARIO VARCHAR(50) NOT NULL,
	SENHA VARCHAR(255),
	PRIMARY KEY (ID)
);

